console.log('Incrementation et Decrementation');

/*--------------------------------------------------------
|------------Incrementation et Decrementation------------|
--------------------------------------------------------*/

/*### Incrementation */
var nb1 = 1;
nb1 = nb1 + 1;
//simplifié
nb1++;
console.log(nb1);

/* ### Decrementation */
var nb1 = nb1 - 1;
//simplifié
nb1--;
console.log(nb1);


/* ### subtilité */
nb1 = 0;
console.log(nb1++);

nb1 = 0;
console.log(++nb1);


